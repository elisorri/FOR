#Forrit biður um tvær tölur
tala1 = int(input("Sláðu inn tölu "))
tala2 = int(input("Sláðu inn aðra tölu "))
summa = tala1 + tala2
margfeldi = tala1 * tala2
#Forrit birtir summu og margfeldi talnana
print(tala1, "+", tala2, "er sama sem", summa)
print(tala1, "*", tala2, "er sama sem", margfeldi)